package mobiliario;

/**
 * Clase que representa un <code>enum</code> con los módulos que se pueden
 * añadir a un mueble personalizable.
 *
 * @author profe
 */
public enum Modulo {
    /**
     * Modulo de tipo Balda.
     */
    BALDA,
    /**
     * Modulo de tipo cajon.
     */
    CAJON,
    /**
     * Modulo de tipo puerta.
     */
    CON_PUERTA,
    /**
     * Modulo de tipo cesto.
     */
    CESTO,
    /**
     * Modulo de tipo zapatero.
     */
    ZAPATERO,
    /**
     * Modulo de tipo bandeja.
     */
    BANDEJA,
    /**
     * Modulo de tipo pantalonero.
     */
    PANTALONERO,
    /**
     * Modulo de tipo barra.
     */
    CON_BARRA
}
